function addCubeAtIdx(scene, idx, idy, idz, wireframe=false, color=0x00ff00) {
    const x = idx;
    const y = idy;
    const z = idz;

    const width = 1;
    const height = 1;
    const depth = 1;

    const geometry = new THREE.BoxGeometry( width, height, depth  );
    const material = new THREE.MeshBasicMaterial( { color: color, wireframe: wireframe});
    const cube = new THREE.Mesh( geometry, material );

    cube.position.set(x, y, z);
    scene.add(cube);
}

for(let z = 0; z < sphereJsonData.value.length; z++) {
    for(let y = 0; y < sphereJsonData.value[z].length; y++) {
        for(let x = 0; x < sphereJsonData.value[z][y].length; x++) {
            const val = sphereJsonData.value[z][y][x];
            addCubeAtIdx(scene, x, y, z, true, 0x00ff00);
            if(val > 0)
                addCubeAtIdx(scene, x, y, z, false, 0xff0000);
        }
    }
}


